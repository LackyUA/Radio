    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplicationLaunchOptionsKey: Any]?) -> Bool {
        
        window = UIWindow(frame: UIScreen.main.bounds)
        if Constants.userDefaults.string(forKey: Constants.userDefaultsDeviceName) != nil {
            window?.makeKeyAndVisible()
            let mainStoryboard: UIStoryboard = UIStoryboard(name: "Main", bundle: nil)
            let mainScreen = mainStoryboard.instantiateViewController(withIdentifier: "MainScreenInit") as! MainScreenViewController
            window?.rootViewController = UINavigationController(rootViewController: mainScreen)
        } else {
            window?.makeKeyAndVisible()
            let mainStoryboard: UIStoryboard = UIStoryboard(name: "Main", bundle: nil)
            let welcomeScreen = mainStoryboard.instantiateViewController(withIdentifier: "WelcomeViewInit") as! WelcomeViewController
            window?.rootViewController = welcomeScreen
        }
        
        // Override point for customization after application launch.
        FirebaseApp.configure()
        Messaging.messaging().delegate = self
        
        application.applicationIconBadgeNumber = 0
        
        GIDSignIn.sharedInstance().clientID = FirebaseApp.app()?.options.clientID

        if #available(iOS 10.0, *) {
            // For iOS 10 display notification (sent via APNS)
            UNUserNotificationCenter.current().delegate = self
            
            let authOptions: UNAuthorizationOptions = [.alert, .badge, .sound]
            UNUserNotificationCenter.current().requestAuthorization(
                options: authOptions,
                completionHandler: {_, _ in })
        } else {
            let settings: UIUserNotificationSettings =
                UIUserNotificationSettings(types: [.alert, .badge, .sound], categories: nil)
            application.registerUserNotificationSettings(settings)
        }
        
        application.registerForRemoteNotifications()
        
        AppData.shared.initPlayer()
        
//        Fabric.with([Crashlytics.self])
//                Fabric.sharedSDK().debug = true
        
        return true
    }
    
